public class Students{
   public static void main(String args[]){
           int marks = 22;
	   System.out.println("initial value is : "+marks);
	   System.out.println("the value before post-increment is : "+marks);
	   marks++;
	   System.out.println("the value after increment is : "+marks);
	   System.out.println("value before post-decrement is : "+marks);
	   marks--;
	   System.out.println("the value after post-decrement is : "+marks);
	  }
 }
	   