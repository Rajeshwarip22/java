public class PostIncreDecre{
   public static void main(String args[]){
       int num = 22;
	   System.out.println("initial value is : "+num);
	   System.out.println("the value before post-increment is : "+num);
	   num++;
	   System.out.println("the value after increment is : "+num);
	   System.out.println("value before post-decrement is : "+num);
	   num--;
	   System.out.println("the value after post-decrement is : "+num);
	  }
 }
	   