public class NumbersDecre{
   public static void main(String args[]){
       int var = 87;
	   System.out.println("initial value is : "+var);
	   ++var;
	   System.out.println("the value after pre-increment is : "+var);
	   System.out.println("value before pre-decrement is : "+var);
	   --var;
	   System.out.println("the value after pre-decrement is : "+var);
	  }
 }
	   