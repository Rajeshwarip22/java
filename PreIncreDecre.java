public class PreIncreDecre{
   public static void main(String args[]){
       int num = 22;
	   System.out.println("initial value is : "+num);
	   ++num;
	   System.out.println("the value after pre-increment is : "+num);
	   System.out.println("value before pre-decrement is : "+num);
	   --num;
	   System.out.println("the value after pre-decrement is : "+num);
	  }
 }
