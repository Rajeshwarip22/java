public class Numbers{
   public static void main(String args[]){
       int var = 87;
	   System.out.println("initial value is : "+var);
	   System.out.println("the value before post-increment is : "+var);
	   var++;
	   System.out.println("the value after increment is : "+var);
	   System.out.println("value before post-decrement is : "+var);
	   var--;
	   System.out.println("the value after post-decrement is : "+var);
	  }
 }
	   